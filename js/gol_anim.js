    function tick() { //main loop
        //console.time("loop");

        ctx.shadowBlur = 0;
        ctx.fillStyle = "white";
        drawGrid();

        ctx.beginPath();
        var dotX = ((gridHeight) * cellSize / 2);
        var dotY = ((23 - 0.5) * cellSize); // Dot is 23 cells down from middle, then some MAGIC crap I don't understand

        if (drawDot) {
            ctx.arc(dotX, dotY, dotRadius, 0, 2 * Math.PI);
            ctx.stroke();
            ctx.fillStyle = "#e43000";

            blurCounter += dotBlurSpeed;
            var blurAmount = ((Math.sin(blurCounter) + 1) * dotMaxBlur) + dotMinBlur;

            ctx.shadowBlur = blurAmount;
            ctx.shadowColor = "#e43000";

            ctx.fill();
        }

        if (simGameOfLife) {
            updateCounter++;
            if (updateCounter >= updateDelay) {
                if (updateDelay > delayMin)
                    updateDelay--;

                updateCounter = 0;
                updateGrid();
                drawDot = false;
            }
        }

        //console.timeEnd("loop");
        requestAnimationFrame(tick);
    }

